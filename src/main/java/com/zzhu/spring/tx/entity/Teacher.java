package com.zzhu.spring.tx.entity;

import lombok.Data;

@Data
public class Teacher {
    private  String name;

    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
